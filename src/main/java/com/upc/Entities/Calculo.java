package com.upc.Entities;

public class Calculo {
	
	private int mes;
	private double fondo;
	private double saldo;
	private double porcflujo;
	private double porcsaldo;
	private double cpf;
	private double cps;
	
	public Calculo(){
		super();
	}

	public int getMes() {
		return mes;
	}

	public void setMes(int mes) {
		this.mes = mes;
	}

	public double getFondo() {
		return fondo;
	}

	public void setFondo(double fondo) {
		this.fondo = fondo;
	}
	
	public double getSaldo() {
		return saldo;
	}

	public void setSaldo(double saldo) {
		this.saldo = saldo;
	}
	
	public double getPorcflujo() {
		return porcflujo;
	}

	public void setPorcflujo(double porcflujo) {
		this.porcflujo = porcflujo;
	}
	
	public double getPorcsaldo() {
		return porcsaldo;
	}

	public void setPorcsaldo(double porcsaldo) {
		this.porcsaldo = porcsaldo;
	}

	public double getCpf() {
		return cpf;
	}

	public void setCpf(double cpf) {
		this.cpf = cpf;
	}
	
	public double getCps() {
		return cps;
	}

	public void setCps(double cps) {
		this.cps = cps;
	}
	
	

}
